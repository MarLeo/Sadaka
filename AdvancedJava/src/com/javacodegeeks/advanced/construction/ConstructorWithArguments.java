package com.javacodegeeks.advanced.construction;

public class ConstructorWithArguments {

	private String arg1;
	private String arg2;
	
	public ConstructorWithArguments(final String arg1,
			final String arg2) {
		this.arg1 = arg1;
		this.arg2 = arg2;
	}

	public String getArg1() {
		return arg1;
	}

	public String getArg2() {
		return arg2;
	}
	
    

}
